    //Load Genders
    private static HashMap<String, String> genderMap = new HashMap<>();

    private void loadGenders() {
        ResultSet rs = null;
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement("Select");

        try {
            rs = DatabaseConnector.executeQuery("SELECT id, name FROM gender");

            while (rs.next()) {
                String name = rs.getString("name");
                genderMap.put(name, rs.getString("id"));
                model.addElement(name);
            }

            genderComboBox.setModel(model);

        } catch (SQLException e) {
            System.err.println("Error loading genders: " + e.getMessage());
            e.printStackTrace();
        } finally {
            DatabaseConnector.closeResources(rs, null, null);
        }
    }

