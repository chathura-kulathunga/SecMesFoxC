    //Add User
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        String userId = userIdField.getText().trim();
        String firstName = firstNameField.getText().trim();
        String lastName = lastNameField.getText().trim();
        String userName = userNameField.getText().trim();
        String password = String.valueOf(passwordField.getPassword());
        String gender = String.valueOf(genderComboBox.getSelectedItem());

        if (userId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Something went wrong!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (firstName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your first name.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lastName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your last name.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select the gender.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (userName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a user name for your account.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter setup a password for your account", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!password.matches("^(?=.*[A-Za-z])(?=.*\\d)[A-Za-z\\d]{8,20}$")) {
            JOptionPane.showMessageDialog(this, "Please enter a password with at least 8-20 characters, \nincluding at least one letter and one number.\nDo not use spaces.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = DatabaseConnector.executeQuery("SELECT COUNT(*) FROM user WHERE user_name = '" + userName + "'");
                rs.next();
                if (rs.getInt(1) > 0) {
                    JOptionPane.showMessageDialog(this, "This username is already in use. Please choose a different username.", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }

                int choice = JOptionPane.showConfirmDialog(this, "Do you want to continue with the registration?", "Confirm Registration", JOptionPane.YES_NO_OPTION);
                if (choice == JOptionPane.YES_OPTION) {
                    int rowsAffected = DatabaseConnector.executeUpdateWithParams(
                            "INSERT INTO user (id, first_name, last_name, user_name, password, created_at, gender_id) "
                            + "VALUES ('" + userId + "', '" + firstName + "', '" + lastName + "', '" + userName + "', '" + password + "', NOW(), '" + genderMap.get(gender) + "')");
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(this,
                                """
                            Registration successful!
                            
                            Please remember your login credentials:
                            Username :  """ + userName + "\n"
                                + "Password : " + password,
                                "Success",
                                JOptionPane.INFORMATION_MESSAGE);
                        reset();
                        new SignIn().setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Failed to register user.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } catch (SQLException e) {
                System.err.println("Error adding new user: " + e.getMessage());
                JOptionPane.showMessageDialog(this, "Database error occurred.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_addButtonActionPerformed

    //Toggle Password
    private boolean showPassword = false;
    private void eyeIconMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eyeIconMouseClicked
        if (showPassword) {
            passwordField.setEchoChar('*');
            ImageIcon icon = new ImageIcon(getClass().getResource("/resources/ico/hidden.png"));
            eyeIcon.setIcon(icon);
        } else {
            passwordField.setEchoChar('\u0000');
            ImageIcon icon = new ImageIcon(getClass().getResource("/resources/ico/view.png"));
            eyeIcon.setIcon(icon);
        }
        showPassword = !showPassword;
    }//GEN-LAST:event_eyeIconMouseClicked

